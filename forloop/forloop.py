#FOR LOOP :A for loop in Python is used to iterate over a sequence (such as a list, tuple, string, range, or other iterable objects) and execute a block of code for each item in the sequence.


###Flow of Execution ::

#The loop takes the first item from the sequence.
#The code block executes.
#The loop moves to the next item.
#This process continues until all items have been processed.
#The loop then terminates.




##Uses of For Loop ::
#Traversing lists, tuples, dictionaries, and strings.
#Repeating a task a fixed number of times.
#Processing data collections.
#Generating patterns and sequences.


###Key Points ::

#for is an iteration-controlled loop.
#It is commonly used when the number of iterations is known.
#It automatically moves through the elements of a sequence.
#Proper indentation is required in Python.



 

