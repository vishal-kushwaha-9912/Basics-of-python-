# CONTAINER : it is a collection of objects. It is a data structure that can hold multiple values or objects.
# Containers are used to store and organize data in a way that allows for easy access and manipulation

# "LIST" : it is a collection of items that are ordered and changeable. It is defined by using square brackets [].
my_list = [1, 2, 3, 4, 5]
print(my_list)

# "TUPLE" : it is a collection of items that are ordered and unchangeable. It is defined by using parentheses ().
my_tuple = ((1, 2, 3, 4, 5))
print(my_tuple)

# "SET" : it is a collection of items that are unordered and unindexed. It is defined by using curly braces {}.
#it is also a collection of unique items, meaning that it cannot contain duplicate items.
my_set = {1, 2, 3, 4, 5}
print(my_set)

# "DICTIONARY" : it helps to searching value fast and very uselfull to programmers. It is a collection of items that are unordered, changeable and indexed. It is defined by using curly braces {}.
# it is a collection of items that are unordered, changeable and indexed. It is defined by using curly braces {}.
my_dict =( {"name":"vishal", "age": 18, "city": "New York"})
print(my_dict)